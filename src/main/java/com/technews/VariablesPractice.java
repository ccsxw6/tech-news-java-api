package com.technews;

public class VariablesPractice {
    public static void main(String[] args) {
        int a = 4;
        double b = 3.5;
        String c = "cat";
        // value of sum is 7.5
        double sum = a + b;
        // string plus number
        String concat = a + c;

        // value of sum is 7.5
        System.out.println(sum);
        // value of concat is "4cat"
        System.out.println(concat);
    }
}
